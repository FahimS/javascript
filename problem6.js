var myScore = 20;


if (myScore >= 80) {
    console.log("A grade");
}
else if (myScore >= 60 && myScore < 79) {
    console.log("B grade");
}
else if (myScore >= 50 && myScore < 69) {
    console.log("C grade");
}
else if (myScore >= 40 && myScore < 59) {
    console.log("D grade");
}
else if (myScore <= 39) {
    console.log("F grade");
}